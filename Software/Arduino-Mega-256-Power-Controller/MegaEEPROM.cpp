//////////////////////////////////////////////////////////////////////////////
// EEPROM Functions
//////////////////////////////////////////////////////////////////////////////
#include <EEPROM.h>
#include "MegaEEPROM.h"
//////////////////////////////////////////////////////////////////////////////
// +| WriteToEEPROM |+
//////////////////////////////////////////////////////////////////////////////
void WriteToEEPROM  ( int    address                                         ,
                      int    len                                             ,
                      char * Buffer                                        ) {
  for               ( int i = 0 ; i < len ; i++                            ) {
    EEPROM . write  ( address + i , Buffer [ i ]                           ) ;
  }                                                                          ;
}
//////////////////////////////////////////////////////////////////////////////
// -| WriteToEEPROM |-
//////////////////////////////////////////////////////////////////////////////
// +| ReadFromEEPROM |+
//////////////////////////////////////////////////////////////////////////////
void ReadFromEEPROM ( int address , int len , char * Buffer                ) {
  for               ( int i = 0 ; i < len ; i++                            ) {
    Buffer [ i ] = EEPROM . read  ( address + i                            ) ;
  }
}
